import 'dotenv/config';
import Database from 'better-sqlite3';
import pg from 'pg';
import path from 'path';
import { fileURLToPath } from 'url';
import { existsSync } from 'fs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const defaultSqlitePath = path.resolve(__dirname, '../data/fabu.db');

const sqlitePath = process.env.SQLITE_DB_PATH || defaultSqlitePath;
const databaseUrl = process.env.DATABASE_URL;

if (!databaseUrl) {
  console.error('DATABASE_URL fehlt. Migration nach PostgreSQL kann nicht gestartet werden.');
  process.exit(1);
}

if (!existsSync(sqlitePath)) {
  console.error(`SQLite-Datei nicht gefunden: ${sqlitePath}`);
  process.exit(1);
}

const sqlite = new Database(sqlitePath, {
  readonly: true,
  fileMustExist: true,
});

const pool = new pg.Pool({ connectionString: databaseUrl });

const PG_SCHEMA = `
  CREATE TABLE IF NOT EXISTS users (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    email TEXT UNIQUE NOT NULL,
    password TEXT NOT NULL,
    role TEXT NOT NULL DEFAULT 'user',
    created_at TIMESTAMPTZ DEFAULT NOW()
  );

  CREATE TABLE IF NOT EXISTS vehicles (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    license_plate TEXT UNIQUE NOT NULL,
    type TEXT NOT NULL DEFAULT 'PKW',
    description TEXT DEFAULT '',
    price_per_km DOUBLE PRECISION NOT NULL DEFAULT 0,
    flat_fee DOUBLE PRECISION,
    active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ DEFAULT NOW()
  );

  CREATE TABLE IF NOT EXISTS reservations (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id INTEGER NOT NULL REFERENCES users(id),
    vehicle_id INTEGER NOT NULL REFERENCES vehicles(id),
    date DATE NOT NULL,
    date_to DATE NOT NULL,
    time_from TIME NOT NULL,
    time_to TIME NOT NULL,
    reason TEXT NOT NULL,
    km_driven INTEGER,
    destination TEXT,
    status TEXT NOT NULL DEFAULT 'reserved',
    created_at TIMESTAMPTZ DEFAULT NOW()
  );

  CREATE INDEX IF NOT EXISTS idx_res_vehicle_date ON reservations(vehicle_id, date);
  CREATE INDEX IF NOT EXISTS idx_res_user ON reservations(user_id);
`;

function toBoolean(value) {
  if (typeof value === 'boolean') {
    return value;
  }
  return Number(value) === 1;
}

function normalizeUser(row) {
  return [
    row.id,
    row.name,
    row.email,
    row.password,
    row.role || 'user',
    row.created_at || null,
  ];
}

function normalizeVehicle(row) {
  return [
    row.id,
    row.name,
    row.license_plate,
    row.type || 'PKW',
    row.description || '',
    Number(row.price_per_km ?? 0),
    row.flat_fee == null ? null : Number(row.flat_fee),
    toBoolean(row.active ?? 1),
    row.created_at || null,
  ];
}

function normalizeReservation(row) {
  return [
    row.id,
    row.user_id,
    row.vehicle_id,
    row.date,
    row.date_to || row.date,
    row.time_from,
    row.time_to,
    row.reason,
    row.km_driven == null ? null : Number(row.km_driven),
    row.destination || null,
    row.status || 'reserved',
    row.created_at || null,
  ];
}

async function ensureSchema(client) {
  await client.query(PG_SCHEMA);
  await client.query('ALTER TABLE vehicles ADD COLUMN IF NOT EXISTS price_per_km DOUBLE PRECISION NOT NULL DEFAULT 0');
  await client.query('ALTER TABLE vehicles ADD COLUMN IF NOT EXISTS flat_fee DOUBLE PRECISION');
  await client.query('ALTER TABLE reservations ADD COLUMN IF NOT EXISTS date_to DATE');
  await client.query('UPDATE reservations SET date_to = date WHERE date_to IS NULL');
}

async function resetSequence(client, tableName) {
  await client.query(
    `
      SELECT setval(
        pg_get_serial_sequence($1, 'id'),
        COALESCE((SELECT MAX(id) FROM ${tableName}), 1),
        EXISTS(SELECT 1 FROM ${tableName})
      )
    `,
    [tableName]
  );
}

async function migrateUsers(client, rows) {
  for (const row of rows) {
    await client.query(
      `
        INSERT INTO users (id, name, email, password, role, created_at)
        VALUES ($1, $2, $3, $4, $5, COALESCE($6::timestamptz, NOW()))
        ON CONFLICT (id) DO UPDATE
        SET name = EXCLUDED.name,
            email = EXCLUDED.email,
            password = EXCLUDED.password,
            role = EXCLUDED.role,
            created_at = EXCLUDED.created_at
      `,
      normalizeUser(row)
    );
  }
}

async function migrateVehicles(client, rows) {
  for (const row of rows) {
    await client.query(
      `
        INSERT INTO vehicles (
          id,
          name,
          license_plate,
          type,
          description,
          price_per_km,
          flat_fee,
          active,
          created_at
        )
        VALUES ($1, $2, $3, $4, $5, $6, $7, $8, COALESCE($9::timestamptz, NOW()))
        ON CONFLICT (id) DO UPDATE
        SET name = EXCLUDED.name,
            license_plate = EXCLUDED.license_plate,
            type = EXCLUDED.type,
            description = EXCLUDED.description,
            price_per_km = EXCLUDED.price_per_km,
            flat_fee = EXCLUDED.flat_fee,
            active = EXCLUDED.active,
            created_at = EXCLUDED.created_at
      `,
      normalizeVehicle(row)
    );
  }
}

async function migrateReservations(client, rows) {
  for (const row of rows) {
    await client.query(
      `
        INSERT INTO reservations (
          id,
          user_id,
          vehicle_id,
          date,
          date_to,
          time_from,
          time_to,
          reason,
          km_driven,
          destination,
          status,
          created_at
        )
        VALUES (
          $1,
          $2,
          $3,
          $4::date,
          $5::date,
          $6::time,
          $7::time,
          $8,
          $9,
          $10,
          $11,
          COALESCE($12::timestamptz, NOW())
        )
        ON CONFLICT (id) DO UPDATE
        SET user_id = EXCLUDED.user_id,
            vehicle_id = EXCLUDED.vehicle_id,
            date = EXCLUDED.date,
            date_to = EXCLUDED.date_to,
            time_from = EXCLUDED.time_from,
            time_to = EXCLUDED.time_to,
            reason = EXCLUDED.reason,
            km_driven = EXCLUDED.km_driven,
            destination = EXCLUDED.destination,
            status = EXCLUDED.status,
            created_at = EXCLUDED.created_at
      `,
      normalizeReservation(row)
    );
  }
}

async function main() {
  const users = sqlite.prepare('SELECT * FROM users ORDER BY id').all();
  const vehicles = sqlite.prepare('SELECT * FROM vehicles ORDER BY id').all();
  const reservations = sqlite.prepare('SELECT * FROM reservations ORDER BY id').all();

  const client = await pool.connect();
  try {
    await client.query('BEGIN');
    await ensureSchema(client);
    await migrateUsers(client, users);
    await migrateVehicles(client, vehicles);
    await migrateReservations(client, reservations);
    await resetSequence(client, 'users');
    await resetSequence(client, 'vehicles');
    await resetSequence(client, 'reservations');
    await client.query('COMMIT');

    console.log(`SQLite Quelle: ${sqlitePath}`);
    console.log(`Benutzer migriert: ${users.length}`);
    console.log(`Fahrzeuge migriert: ${vehicles.length}`);
    console.log(`Reservierungen migriert: ${reservations.length}`);
  } catch (error) {
    await client.query('ROLLBACK');
    throw error;
  } finally {
    client.release();
  }
}

main()
  .catch((error) => {
    console.error('Migration SQLite -> PostgreSQL fehlgeschlagen.');
    console.error(error);
    process.exitCode = 1;
  })
  .finally(async () => {
    sqlite.close();
    await pool.end();
  });